function zoomListing(obj){
    obj.style.color="whitesmoke"
    obj.style.transform="scale(1.1)"
    obj.style.backgroundPosition="-60px"
    obj.style.textShadow="0 3px 6px rgba(0, 0, 0, 0.16), 0 3px 6px rgba(0, 0, 0, 0.23)"
}
function returnZoom(obj){
    obj.style.color="#909090"
    obj.style.transform="scale(1)"
    obj.style.backgroundPosition="-0"
    obj.style.textShadow="0"
}
